import UIKit


extension UILabel {
    func applyNightBlue() {
        self.textColor = UIColor.nightBlue
    }
    
    func applyDarkBlue() {
        self.textColor = UIColor.darkBlue
    }
    func applySeaBlue() {
        self.textColor = UIColor.seaBlue
    }
    func applySkyBlue() {
        self.textColor = UIColor.skyBlue
    }
    
    
    
    
}
